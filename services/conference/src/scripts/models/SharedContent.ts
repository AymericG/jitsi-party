import {MapObject} from './MapObject'

export interface SharedContent extends MapObject {
  id: string    //  unique ID (generated by participant id + number)
  type: string  //  object type ('img', etc)
  url: string   //  url or text to share
  size: [number, number]
}

export interface SharedContents {
  contents: {
    [key: string]: SharedContent,
  }
  renderOrder: string[] // array of shared content id, higer index will be rendered more front
}
